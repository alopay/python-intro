# dynamic variable type in python
# manipulating varibale data type

m = 1
# 'type' to check for data types

print(m)
print(type(m))

m = "Holla" # this is a string

print(m)
print(type(m))

m = 0.01 # this is a float/decimal
print(m)
print(type(m))

m = True # this is a boolean
print(m)
print(type(m))

n = m
print(n)
print(type(n))
